// String and Number Concatenation in JavaScript

console.log("1" + 1);         // "11" → "1" (string) + 1 (number) → "11"
console.log(1 + "3");         // "13" → 1 (number) + "3" (string) → "13"
console.log("1" + "3");       // "13" → "1" + "3" → both are strings
console.log("1" + 3 + 3);     // "133" → "1" + 3 = "13", then "13" + 3 = "133"
console.log(1 + 3 + "3");     // "43" → 1 + 3 = 4 (number addition), then 4 + "3" = "43"


